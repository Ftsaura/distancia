/** Comentario de la clase1*/

public class MiClase {

    // Este método calcula la suma de dos números enteros
    // y devuelve el resultado.
    // Parámetros:
    //   a - El primer número entero a sumar.
    //   b - El segundo número entero a sumar.
    // Devuelve:
    //   La suma de los dos números enteros.
    public int sumar(int a, int b) {
        return a + b;
    }

    // Este método concatena dos cadenas de texto y devuelve el resultado.
    // Parámetros:
    //   cadena1 - La primera cadena de texto a concatenar.
    //   cadena2 - La segunda cadena de texto a concatenar.
    // Devuelve:
    //   La concatenación de las dos cadenas de texto.
    public String concatenar(String cadena1, String cadena2) {
        return cadena1 + cadena2;
    }

    // Este método verifica si un número entero es positivo.
    // Parámetros:
    //   num - El número entero a verificar.
    // Devuelve:
    //   true si el número es positivo, false de lo contrario.
    public boolean esPositivo(int num) {
        return num > 0;
    }
}